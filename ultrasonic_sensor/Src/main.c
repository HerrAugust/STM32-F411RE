/**
 * Waits that the signal of echo goes to high, starts a timer
 * and waits the echo signal to go back high.
 * It returns the length of the pulse in microseconds
 * @return length of the pulse in microseconds
 */
uint32_t pulseIn(void) {
    uint32_t prevTick, curTick;
    unsigned char buffer[64] = "in pulseIn()";
    HAL_UART_Transmit(&huart1, buffer, sizeof(buffer), HAL_MAX_DELAY);

    while (HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_8) == GPIO_PIN_RESET)
        ;
    sprintf(buffer, "now high\r\n");
    HAL_UART_Transmit(&huart1, buffer, sizeof(buffer), HAL_MAX_DELAY);
    // Now signal is high. Wait it goes low
    prevTick = HAL_GetTick();
    while (HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_8) ==  GPIO_PIN_SET)
        ;
    sprintf(buffer, "now low\r\n");
    HAL_UART_Transmit(&huart1, buffer, sizeof(buffer), HAL_MAX_DELAY);
    // Now signal is low
    curTick = HAL_GetTick();
    return curTick - prevTick;
}

/**
 * Time to distance
 * @param duration pulse duration
 * @return the distance
 */
uint8_t getDistance(uint8_t duration) {
    return duration * 0.034 / 2; // /2 because the signal bounces on the obstacle
}

void delay_ticks(unsigned ticks)
{
    SysTick->LOAD = ticks;
    SysTick->VAL = 0;
    SysTick->CTRL = SysTick_CTRL_ENABLE_Msk;

    // COUNTFLAG is a bit that is set to 1 when counter reaches 0.
    // It's automatically cleared when read.
    while ((SysTick->CTRL & SysTick_CTRL_COUNTFLAG_Msk) == 0);
    SysTick->CTRL = 0;
}

